#ifndef __UART1_PARSER_H
#define __UART1_PARSER_H

#include <stdint.h>
#include <stdbool.h>

/***********************************************************************
 * UART1 树莓派 ASCII 协议解析器 模块
 *
 * 协议格式: a<带符号整数X>b<带符号整数Y>c
 *   示例:   "a-15b23c"  →  X=-15, Y=23
 *           "a100b200c" →  X=100, Y=200
 *
 * X/Y 分别表示靶心在相机画面中的像素偏移量（X=水平, Y=垂直）。
 * 解析结果输出到 VisionData 结构体，供步进云台打靶控制使用。
 *
 * 作者：重构自无名创新 ncontroller_v6
 * 日期：2025-05-20
 ***********************************************************************/

/* 协议解析超时时间（毫秒）
 * 当状态机处于 PARSE_X 或 PARSE_Y 状态下，超过此时间未收到后续字节，
 * 则自动复位到 PARSE_IDLE，丢弃不完整帧，等待下一个 'a' 帧头。
 * 彻底解决了原 parse_char() 收到 'a' 后永远卡在中间状态的问题。 */
#define UART1_PARSER_TIMEOUT_MS  100

/***********************************************************************
 * 解析器状态枚举
 * PARSE_IDLE: 空闲，等待帧头 'a'
 * PARSE_X:    正在解析第一个整数值（X 偏移）
 * PARSE_Y:    正在解析第二个整数值（Y 偏移）
 ***********************************************************************/
typedef enum {
    PARSE_IDLE = 0,   /* 空闲，等待 'a' */
    PARSE_X,          /* 解析 X 值，等待 'b' 或数字 */
    PARSE_Y           /* 解析 Y 值，等待 'c' 或数字 */
} uart1_parser_state_t;

/***********************************************************************
 * 视觉数据结构体
 * 存储从树莓派解析出的靶心偏移量，供步进电机打靶控制使用。
 *
 * valid 标志：
 *   true  = 成功接收到一个完整帧，x/y 有效
 *   false = 尚未收到任何完整帧（初始化或通信中断后）
 *   注意：valid 一旦置 true 后不会自动清除，表示"曾经收到过有效数据"。
 *         步进电机在 valid=false 时使用 x=0, y=0（保持云台位置不动）。
 ***********************************************************************/
typedef struct {
    int16_t  x;              /* 靶心 X 方向像素偏移（有符号，像素）        */
    int16_t  y;              /* 靶心 Y 方向像素偏移（有符号，像素）        */
    uint32_t timestamp_ms;   /* 数据接收时间戳（millis()，用于诊断）       */
    bool     valid;          /* 数据有效性标志（true=收到过完整帧）         */
} VisionData;

/***********************************************************************
 * 函数名: void uart1_parser_init(void)
 * 说明:   初始化解析器状态机。
 *         将所有内部状态重置为初始值，清空 VisionData。
 * 入口:   无
 * 出口:   无
 * 备注:   在 main() 初始化阶段调用一次
 ***********************************************************************/
void uart1_parser_init(void);

/***********************************************************************
 * 函数名: void uart1_parser_feed(void)
 * 说明:   从环形缓冲区取字节喂入状态机。
 *         在 200Hz 任务中调用（紧随 uart1_dma_process() 之后）。
 *         通过 while(uart1_dma_get_byte(&byte)) 批量消费所有可用字节，
 *         避免多次函数调用的开销。
 * 入口:   无
 * 出口:   无
 * 备注:   本函数内部无阻塞，无字节时立即返回。
 ***********************************************************************/
void uart1_parser_feed(void);

/***********************************************************************
 * 函数名: const VisionData* uart1_parser_get_data(void)
 * 说明:   获取最新解析出的视觉数据只读指针。
 *         调用方（step_motor_ctrl_dianshe）通过此函数读取靶心偏移量，
 *         替代原来直接访问 values[] 的方式。
 * 入口:   无
 * 出口:   指向 VisionData 的只读指针（始终有效，不会返回 NULL）
 * 备注:   返回的指针指向内部静态变量，不需要调用方释放。
 ***********************************************************************/
const VisionData *uart1_parser_get_data(void);

#endif /* __UART1_PARSER_H */
