#ifndef __UART1_DMA_H
#define __UART1_DMA_H

#include <stdint.h>

/***********************************************************************
 * UART1 DMA + 环形缓冲区 驱动模块
 * 功能：使用 DMA CH1 异步接收 UART1（树莓派视觉）数据，
 *       配合环形缓冲区解耦 DMA 与协议解析器。
 * 作者：重构自无名创新 ncontroller_v6
 * 日期：2025-05-20
 ***********************************************************************/

/* DMA 通道与触发源 */
#define UART1_DMA_CHANNEL       1               /* 使用 DMA 通道 1（FULL 通道，支持 REPEAT 模式） */
#define UART1_DMA_TRIGGER       DMA_UART1_RX_TRIG /* UART1 RX DMA 触发源 = 19                */
#define UART1_DMA_BUF_SIZE      256              /* DMA 硬件缓冲区和环形缓冲区大小（2的幂）   */

/***********************************************************************
 * 函数名: void uart1_dma_init(void)
 * 说明:   DMA CH1 一次性初始化。配置 REPEAT_SINGLE 模式，
 *         使能 UART1 DMA 接收事件，初始化环形缓冲区。
 * 入口:   无
 * 出口:   无
 * 备注:   在 main() 中 timer_irq_config() 之后调用一次
 ***********************************************************************/
void uart1_dma_init(void);

/***********************************************************************
 * 函数名: void uart1_dma_process(void)
 * 说明:   200Hz 定时器任务中调用。短暂禁用 DMA，将 DMA 硬件缓冲区中
 *         新接收的字节拷入环形缓冲区，重置 DMA 目标地址和传输计数，
 *         然后重新使能 DMA。
 * 入口:   无
 * 出口:   无
 * 备注:   DMA 禁用窗口 < 10us，UART 硬件 FIFO 足够缓冲（115200bps 下
 *          单字节 87us）。每 5ms 最多收 ~58 字节，256 字节缓冲安全。
 ***********************************************************************/
void uart1_dma_process(void);

/***********************************************************************
 * 函数名: int uart1_dma_get_byte(uint8_t *byte)
 * 说明:   从环形缓冲区非阻塞取一字节，供协议解析器使用。
 *         使用 __disable_irq()/__enable_irq() 临界区保护
 *         读写指针（200Hz ISR 与主循环间的并发访问）。
 * 入口:   byte - 输出字节指针
 * 出口:   1 = 读取成功，0 = 缓冲区空
 * 备注:   在 uart1_parser_feed() 的 while 循环中调用
 ***********************************************************************/
int  uart1_dma_get_byte(uint8_t *byte);

/***********************************************************************
 * 函数名: int uart1_dma_available(void)
 * 说明:   返回环形缓冲区中当前可读字节数（用于调试诊断）。
 * 入口:   无
 * 出口:   缓冲区中待读取的字节数
 ***********************************************************************/
int  uart1_dma_available(void);

#endif /* __UART1_DMA_H */
